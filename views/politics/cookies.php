            <h3 class="fw-bold">1. What Are Cookies</h3>
            <p>Cookies are <strong>small text files</strong> stored on your device (computer, tablet, phone) when you visit a <strong>website</strong>.
                They help websites <strong>remember things</strong> like:</p>
            <ul>
                <li>Who you are.</li>
                <li>What you like.</li>
                <li>What page you were on.</li>
                <li>And even how you like <strong>your content</strong> served.</li>
            </ul>
